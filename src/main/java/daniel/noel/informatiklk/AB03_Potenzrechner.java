    private void calcButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcButtonActionPerformed
        int base = Integer.parseInt(this.numberInput.getText());
        int exponent = Integer.parseInt(this.exponentInput.getText());
        int result = 1;
        
        for (int i = 0; i <  Math.abs(exponent); i++) {
            result = result * base;
        }
        
        if (exponent < 0) {
            double newResult = result;
            newResult = 1 / newResult;
            this.resultOutputLabel.setText("" + newResult);
        } else {
            this.resultOutputLabel.setText("" + result);
        }
                
    }//GEN-LAST:event_calcButtonActionPerformed

